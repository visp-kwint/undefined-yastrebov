// ============ HELPER ============
export function getFileIconComponent(filename: string, size = 18) {
  const ext = filename.split(".").pop()?.toLowerCase() || "";
  if (ext === "pdf") return <FilePdfIcon size={size} />;
  if (["docx", "doc"].includes(ext)) return <FileWordIcon size={size} />;
  if (["xlsx", "xls", "csv"].includes(ext)) return <FileExcelIcon size={size} />;
  if (["jpg", "jpeg", "png", "gif", "webp"].includes(ext)) return <FileImageIcon size={size} />;
  if (["txt", "md"].includes(ext)) return <FileTextIcon size={size} />;
  return <FileGenericIcon size={size} />;
}